package org.openecomp.sdc.ci.tests.datatypes.expected;

public class ExpectedDistDownloadAudit {

	String action;
	String consumerId;
	String resourceUrl;
	String status;
	String desc;

	public ExpectedDistDownloadAudit(String action, String consumerId, String resourceUrl, String status, String desc) {
		super();
		this.action = action;
		this.consumerId = consumerId;
		this.resourceUrl = resourceUrl;
		this.status = status;
		this.desc = desc;
	}

	public String getAction() {
		return action;
	}

	public void setAction(String action) {
		this.action = action;
	}

	public String getConsumerId() {
		return consumerId;
	}

	public void setConsumerId(String consumerId) {
		this.consumerId = consumerId;
	}

	public String getResourceUrl() {
		return resourceUrl;
	}

	public void setResourceUrl(String resourceUrl) {
		this.resourceUrl = resourceUrl;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
}
